/**
 * The Status enumeration represents the status of a work.
 * Possible values are: EXHIBITED, INVENTORY, BORROWED, RESTORATION.
 * 
 * @author Javier Gael Luis
 */
package works;

public enum Status {
    /**
     * The work is exhibited.
     */
    EXHIBITED,
    
    /**
     * The work is in inventory.
     */
    INVENTORY,
    
    /**
     * The work is borrowed.
     */
    BORROWED,
    
    /**
     * The work is under restoration.
     */
    RESTORATION
}
